#include<stdio.h>
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Display numbers reverse upto given number.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void DisplayB(int iNo)
{
    int iCnt=iNo;
    while(iCnt>=1)
    {
        printf("%d\t",iCnt);
        iCnt--;
    }

    
}

int main()
{
   int iValue=0;
   printf("Enter number : ");
   scanf("%d",&iValue);

   DisplayB(iValue);


    
    return 0;
}