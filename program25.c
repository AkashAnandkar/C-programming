////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Problem Statement : Find the addition of factors of given number.
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////

 int SumFactors(int iNo)
{

    int iCnt=0;
    int iSum=0;

    if(iNo < 0)
    {
        iNo=-iNo;    //updater
    }

    for(iCnt=1; iCnt<=(iNo/2); iCnt++)
    {
        if((iNo % iCnt)==0)
        {
            iSum=iSum+iCnt;
        }
    }
    return iSum;

}

